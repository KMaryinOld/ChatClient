public class Theme {
    protected int titleBackgroundColor;
    protected int titleTextColor;
    protected int borderColor;
    protected int roomChatColor;
    protected int guestRoomTextColor;
    protected int guestRoomBackgroundColor;
    protected int guestRoomSeparatorColor;
    protected int guestRoomItemDark;
    protected int guestRoomItemLight;

    protected int usernameTextColor;

    public int getTitleBackgroundColor() {
        return titleBackgroundColor;
    }

    public int getTitleTextColor() {
        return titleTextColor;
    }

    public int getBorderColor() {
        return borderColor;
    }

    public int getRoomChatColor() {
        return roomChatColor;
    }

    public int getGuestRoomTextColor() {
        return guestRoomTextColor;
    }

    public int getGuestRoomBackgroundColor() {
        return guestRoomBackgroundColor;
    }

    public int getGuestRoomSeparatorColor() {
        return guestRoomSeparatorColor;
    }

    public int getGuestRoomItemDark() {
        return guestRoomItemDark;
    }

    public int getGuestRoomItemLight() {
        return guestRoomItemLight;
    }

    public int getUsernameTextColor() {
        return usernameTextColor;
    }
}
